# Operadores aritmeticos
22 + 22  # Suma
22 - 2  # Resta
22 / 2  # Divison
22 // 2  # Divison entera
22 * 2  # Multiplicacion
22 % 2  # Modulo
22 ** 2  # Exponente

# Operadores de asignacion
numero = 0
numero += 2
numero -= 1
numero *= 9
numero /= 3

# Operadores de comparacion
numero1 = 5
numero2 = 3
print(numero1 == numero2)
print(numero1 != numero2)
print(numero1 > numero2)
print(numero1 < numero2)
print(numero1 >= numero2)
print(numero1 <= numero2)
